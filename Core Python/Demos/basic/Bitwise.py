## Bitwise Operator

# 1. & (AND)
# if both bit are 1 then consider 1 otherwise 0
print(13 & 6)

# 2. | (OR)
# if both bit are 0 then consider 0 otherwise 1
print(13 | 6)

# 3. ^ (XOR)
# if both bit are different then consider 1 otherwise 0
print(13 ^ 6)

# 4. ~ (NOT)
print(~10)  #----> -(10+1)---> -11
print(~45)  #----> -(45+1)---> -46