## Bitwise Operator

# 1. & (AND)
# if both bit are 1 then consider 1 otherwise 0
print(13 & 6)

# 2. | (OR)
# if both bit are 0 then consider 0 otherwise 1
print(13 | 6)

# 3. ^ (XOR)
# if both bit are different then consider 1 otherwise 0
print(13 ^ 6)

# 4. ~ (NOT)
print(~10)  #----> -(10+1)---> -11
print(~45)  #----> -(45+1)---> -46

# << (Left shift)
print(3<<2)     #first convert 3 into binary then shifts bits towords left by 2 

# << (Right shift)
print(20>>2)     #first convert 20 into binary then shifts bits towords right by 2 