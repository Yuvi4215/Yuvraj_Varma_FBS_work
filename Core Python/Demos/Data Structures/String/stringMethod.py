### String Method 
strng="Each Words Starts With Capital Case"



# 1. capitalize()
print(strng.capitalize()) 

# 2. count(char)
print(strng.count("s")) 

# 3. endsWith(char)
print(strng.endswith("se")) 

# 4. find(char)
print(strng.find("s")) 

# 5. index(char)
print(strng.index("s"))

# 6. isalnum()
print(strng.isalnum()) 

# 7. isdigit()
print(strng.isdigit())

# 8. islower()
print(strng.islower()) 

# 9. isspace()
print(strng.isspace())

# 10. isupper()
print(strng.isupper()) 


li=["a", "b", "c", "d"]
# 11. join(list)
print(", ".join(li))

# 12. lower()
print(strng.lower()) 

# 13. replace(what, with what)
print(strng.replace("with", "WITH"))

# 14. split()
print(strng.split(", ")) 